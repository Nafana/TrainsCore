package net.nafana.TrainsCore.utils;

/**
 * @author Created by Nafana on 2018-07-07.
 */
public class Vector2D {

    private int x;
    private int y;

    /**
     * Instantiates a new Vector2D.
     *
     * @param x the x
     * @param y the y
     */
    public Vector2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Gets x.
     *
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Sets x.
     *
     * @param x the x
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * Sets y.
     *
     * @param y the y
     */
    public void setY(int y) {
        this.y = y;
    }
}
